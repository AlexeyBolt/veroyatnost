import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
zp = np.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
ks = np.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])
plt.scatter(zp,ks)
plt.xlabel('Величина заработной платы')
plt.ylabel('Поведенческий кредитный скоринг')
plt.show()
cov = np.mean(zp * ks) - np.mean(zp) * np.mean(ks)
print(cov)
print(np.cov(zp, ks, ddof=0))
print(np.cov(zp, ks, ddof = 1))
print(np.std(zp, ddof = 1))
print(np.std(ks, ddof = 1))
print(10175.37777778/(62.31318747531162 * 183.99544076477068))
print(np.cov(zp, ks, ddof = 0))
print(np.std(zp, ddof = 0))
print(np.std(ks, ddof = 0))
print(9157.84/(59.115480206118605 * 174.55340157098058))
print(np.corrcoef(zp,ks))
df = pd.DataFrame(data = {'zp':zp, 'ks':ks})
print(df)
print(df.corr())
print('существует сильная линейная взаимосвязь между исходными данными')